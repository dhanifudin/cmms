/**
 * Generate asset code based on category and terminal
 */
export function generateAssetCode(category: string, terminalId: string): string {
  const categoryPrefixes: Record<string, string> = {
    pump: 'PP',
    compressor: 'GC',
    pipeline: 'PL',
    valve: 'VL',
    tank: 'TK',
    generator: 'GN',
    electrical: 'EL',
    instrumentation: 'IN',
    safety_system: 'SS',
    hvac: 'HV',
    vehicle: 'VH',
    tool: 'TL',
    other: 'OT'
  };

  const terminalPrefixes: Record<string, string> = {
    terminal1: 'JKT',
    terminal2: 'SBY',
    terminal3: 'MDN',
    terminal4: 'PLB',
    terminal5: 'BJM',
    terminal6: 'MKS'
  };

  const prefix = categoryPrefixes[category] || 'OT';
  const terminalPrefix = terminalPrefixes[terminalId] || 'XXX';
  const sequence = String(Math.floor(Math.random() * 999) + 1).padStart(3, '0');

  return `${prefix}-${terminalPrefix}-${sequence}`;
}
