// Helper functions for terminal operations
export const getTerminalById = (terminalId: string): Terminal | undefined => {
  return mockTerminals.find(terminal => terminal.id === terminalId);
};

export const getTerminalsByRegion = (regionId: string): Terminal[] => {
  return mockTerminals.filter(terminal => terminal.regionId === regionId);
};

export const getActiveTerminals = (): Terminal[] => {
  return mockTerminals.filter(terminal => terminal.active);
};

export const getTerminalsByType = (type: 'depot' | 'station' | 'hub'): Terminal[] => {
  return mockTerminals.filter(terminal => terminal.type === type);
};

export const getTerminalRegion = (terminalId: string): string | undefined => {
  const terminal = getTerminalById(terminalId);
  return terminal ? terminal.regionId : undefined;
};

// Terminal statistics
export const getTerminalStatistics = () => {
  return {
    totalTerminals: mockTerminals.length,
    activeTerminals: mockTerminals.filter(t => t.active).length,
    terminalsByType: {
      depot: mockTerminals.filter(t => t.type === 'depot').length,
      station: mockTerminals.filter(t => t.type === 'station').length,
      hub: mockTerminals.filter(t => t.type === 'hub').length
    },
    terminalsByRegion: mockTerminals.reduce((acc, terminal) => {
      acc[terminal.regionId] = (acc[terminal.regionId] || 0) + 1;
      return acc;
    }, {} as Record<string, number>),
    totalCapacity: mockTerminals.reduce((sum, terminal) => sum + (terminal.capacity || 0), 0),
    averageCapacity: Math.round(mockTerminals.reduce((sum, terminal) => sum + (terminal.capacity || 0), 0) / mockTerminals.length)
  };
};